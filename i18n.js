window.LBi18n = {
  currentLang: 'EN', // EN, JP

  init() {
    // Listen to region changes to toggle language mode
    document.addEventListener('lb:regionChanged', (e) => {
      const code = e.detail.code;
      if (code === 'JP') {
        this.setLanguage('JP');
      } else {
        this.setLanguage('EN');
      }
    });

    // Run initially based on GeoRouter
    if (typeof GeoRouter !== 'undefined') {
      const current = GeoRouter.get();
      if (current === 'JP') this.setLanguage('JP');
    }
  },

  setLanguage(langCode) {
    this.currentLang = langCode;
    console.log('[i18n] Switched to', langCode);
    
    // Process static DOM elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (!el.hasAttribute('data-i18n-orig')) {
        el.setAttribute('data-i18n-orig', el.innerHTML); // Store original English (or original HTML)
      }

      if (langCode === 'JP' && window.LB_TRANSLATIONS.JP[key]) {
        el.innerHTML = window.LB_TRANSLATIONS.JP[key];
      } else {
        el.innerHTML = el.getAttribute('data-i18n-orig'); // Return to original English
      }
    });

    // Notify custom generic product renderers (like in shop.html or region.js)
    // We already have an event lb:regionChanged which triggers updateHomeProducts and renderRegionalPrices.
  },

  /**
   * Helper function for dynamic JS template rendering.
   * i18n('prod.250g.name', p.name) => translates if JP, else fallback English.
   */
  t(key, fallback) {
    if (this.currentLang === 'JP' && window.LB_TRANSLATIONS.JP[key]) {
      return window.LB_TRANSLATIONS.JP[key];
    }
    return fallback;
  }
};

document.addEventListener('DOMContentLoaded', () => {
  window.LBi18n.init();
});

